# Python program to convert the temperature in degree centigrade to fahrenheit.

# f  = c * 9/5 + 32

# Input:
# enter value in centigrade: 37

# Output:
# Value in fahrenheit:  98.6

centigrade = int(input("enter value in centigrade: "))

fahrenheit = (centigrade * 9)/5 + 32

print("Value in fahrenheit: ", fahrenheit)